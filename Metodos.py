#Metodos y funciones#

texto="Hola Mundo"

print(texto.upper())#cambia a mayusculas
print(texto.lower())#cambia a minusculas
print(texto.find("M"))#devuelve el indice en donde esta la letra
print(texto.replace("Mun","prueba"))#remplaza una cadena de caracteres desde la posicion buscada
print("Mundo" in texto)#busca contenido en una variable, devuele true o false